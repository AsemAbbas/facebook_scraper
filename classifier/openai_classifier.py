def classify_posts(
    posts: list[dict],
    openai_token: str,
    model: str = DEFAULT_MODEL,
    batch_size: int = DEFAULT_BATCH_SIZE,
    cancel_callback=None,
) -> list[dict]:
    """
    يصنّف قائمة منشورات. كل منشور يجب أن يحتوي على {id, text}.
    يرجع قائمة [{id, category}] بنفس ترتيب الإدخال.

    posts: list of dicts with 'id' and 'text' keys
    openai_token: OpenAI API key
    model: 'gpt-4o' (default), 'gpt-4o-mini', etc.
    batch_size: عدد المنشورات لكل API call
    cancel_callback: callable يرجع True للإلغاء

    على فشل: يرجع category="" للمنشورات اللي ما اتصنفت.
    """
    if not posts:
        return []
    if not openai_token:
        raise ClassifierError("OpenAI token مطلوب")

    return asyncio.run(_classify_async(
        posts, openai_token, model, batch_size, cancel_callback
    ))


async def _classify_async(
    posts: list[dict],
    openai_token: str,
    model: str,
    batch_size: int,
    cancel_callback,
) -> list[dict]:
    """نسخة async للتصنيف — تشغل batches بالتوازي محدود"""
    results: list[dict] = []
    headers = {
        "Authorization": f"Bearer {openai_token}",
        "Content-Type": "application/json",
    }

    async with aiohttp.ClientSession() as session:
        # نقسم لـ batches
        for i in range(0, len(posts), batch_size):
            if callable(cancel_callback) and cancel_callback():
                break
            batch = posts[i:i + batch_size]
            try:
                batch_results = await _classify_batch(session, batch, headers, model)
                results.extend(batch_results)
            except Exception as e:
                # على فشل batch، نضع category="" لكل عناصره
                print(f"  ⚠️  [classifier] فشل batch {i // batch_size + 1}: {e}")
                for item in batch:
                    results.append({"id": item.get("id", ""), "category": ""})

    return results


async def _classify_batch(
    session: aiohttp.ClientSession,
    batch: list[dict],
    headers: dict,
    model: str,
) -> list[dict]:
    """يصنّف batch واحد عبر OpenAI Chat Completions"""
    # حضّر الـ input JSON بنفس البنية المتوقعة في البرومت
    input_data = {
        "data": [
            {
                "id": str(p.get("id", "")),
                "text": (p.get("text", "") or "")[:MAX_TEXT_CHARS],
                "category": "",
            }
            for p in batch
        ]
    }

    payload = {
        "model": model,
        "messages": [
            {"role": "system", "content": SYSTEM_PROMPT},
            {"role": "user", "content": json.dumps(input_data, ensure_ascii=False)},
        ],
        "temperature": 0,
        "response_format": {"type": "json_object"},
    }

    last_err = None
    for attempt in range(3):
        try:
            async with session.post(
                OPENAI_API_URL,
                headers=headers,
                json=payload,
                timeout=aiohttp.ClientTimeout(total=REQUEST_TIMEOUT),
            ) as r:
                if r.status == 429:
                    # rate limit — انتظر ثم أعد المحاولة
                    await asyncio.sleep(5 * (attempt + 1))
                    continue
                if r.status != 200:
                    body = await r.text()
                    last_err = f"HTTP {r.status}: {body[:200]}"
                    if r.status >= 500:
                        await asyncio.sleep(2)
                        continue
                    raise ClassifierError(last_err)
                data = await r.json()
                content = data["choices"][0]["message"]["content"]
                parsed = json.loads(content)
                return _validate_results(parsed, batch)
        except asyncio.TimeoutError:
            last_err = "timeout"
            continue
        except Exception as e:
            last_err = str(e)
            if attempt < 2:
                await asyncio.sleep(2)
                continue
            raise ClassifierError(f"فشل التصنيف بعد 3 محاولات: {e}")

    raise ClassifierError(f"فشل التصنيف بعد 3 محاولات: {last_err}")


def _validate_results(parsed: dict, batch: list[dict]) -> list[dict]:
    """يتأكد أن النتائج صحيحة + categories من القائمة المسموحة"""
    out_data = parsed.get("data") if isinstance(parsed, dict) else None
    if not isinstance(out_data, list):
        # OpenAI رجّع شيء غير متوقع
        return [{"id": str(p.get("id", "")), "category": ""} for p in batch]

    # نطابق بـ id (قد لا يحافظ على الترتيب)
    by_id = {}
    for item in out_data:
        if not isinstance(item, dict):
            continue
        pid = str(item.get("id", ""))
        cat = str(item.get("category", "")).strip()
        # تحقق من validity — لو خارج القائمة، نعتبره فارغ
        if cat and cat not in CATEGORIES_SET:
            # محاولة matching بدون تطبيع: لو الـ category بفراغات أو علامات مخالفة
            cleaned = cat.strip().strip('"').strip("'")
            if cleaned in CATEGORIES_SET:
                cat = cleaned
            else:
                cat = ""  # invalid — fallback
        by_id[pid] = cat

    # نرجع بنفس ترتيب الـ batch الأصلي
    results = []
    for p in batch:
        pid = str(p.get("id", ""))
        results.append({"id": pid, "category": by_id.get(pid, "")})
    return results


